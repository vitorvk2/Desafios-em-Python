#Exemplo

def cont(a=0, b=0, c=0):
    s = a+b+c
    return s

r1 = cont(1,742,7)
r2 = cont(2,1,5)
r3 = cont(7,4,54)

print(f'Os meus resultados deram {r1}, {r2} e {r3}')

#O 'return' retorna o valor obtido na função para uma determinada variável
#que poderá ser utilizada posteriormente no andamento do programa

